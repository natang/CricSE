package com.cricket;

public class DataClass {
String name;
int price;
int shares;
public void setName(String name) {
	this.name = name;
}
public void setPrice(int price) {
	this.price = price;
}
public void setShares(int shares) {
	this.shares = shares;
}
public String getName() {
	return name;
}
public int getPrice() {
	return price;
}
public int getShares() {
	return shares;
}
}
