package com.cricket;

public class Portfolio 
{
	int id;
	String ShareName;
	int NoOfShares;
	public int getId()
	{
		return id;
	}
	public void setId(int id)
	{
		this.id = id;
	}
	public String getShareName()
	{
		return ShareName;
	}
	public void setShareName(String shareName)
	{
		ShareName = shareName;
	}
	public int getNoOfShares()
	{
		return NoOfShares;
	}
	public void setNoOfShares(int noOfShares)
	{
		NoOfShares = noOfShares;
	}
}
